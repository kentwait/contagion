func sampleIntrahostModel() IntrahostModel {
	model := new(ConstantPopModel)
	model.mutationRate = 1e-5
	model.transitionMatrix = [][]float64{
		[]float64{0, 1},
		[]float64{1, 0},
	}
	model.recombinationRate = 0
	model.popSize = 1000
	return model
}

